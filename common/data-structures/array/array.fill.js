// fill метод заповнює масив заданим значенням
// використовується для ініціалізації масиву або для перезапису значень масиву
// перший аргумент це значення для заповнення
// другий аргумент це індекс, з якого починається заповнення
// третій аргумент це індекс, на якому закінчується заповнення

const array = [1, 2, 3, 4, 5];
console.log(array.fill(0));

