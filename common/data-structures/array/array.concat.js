// concat метод об'єднує два або більше масивів
// повертає новий масив
// не змінює оригінальні масиви

const array1 = [1, 2, 3];
const array2 = [4, 5, 6];
console.log(array1.concat(array2));

//--------------------------------

const array3 = [1, 2, 3];
const array4 = [4, 5, 6];
const array5 = [1, 2, 3];
console.log(array3.concat(array4, array5));